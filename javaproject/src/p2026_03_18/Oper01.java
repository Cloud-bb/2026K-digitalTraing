package p2026_03_18;

public class Oper01 {

	public static void main(String[] args) {
		int a=10,b=3,c;
		
		c= a+b;
		System.out.println("a+b="+c);
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a/b="+(a/b)); //몫
		System.out.println("a%b="+(a%b)); //나머지
	}

}
