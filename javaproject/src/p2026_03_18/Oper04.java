package p2026_03_18;

public class Oper04 {
	public static void main(String[] args) {
		
		//비교 연산자 : >, >=, <, <=, ==, !=
		//비교 연산자의 값이 참이면  true, 거짓이면 false 를 반환
		int n1 = 10;
		int n2 = 20;
		
		if(n1>n2) {
			System.out.println(n1+"은(는)"+n2+"보다 크다");
		}else {
			System.out.println(n1+"은(는)"+n2+"보다 작다");
		}
	}

}